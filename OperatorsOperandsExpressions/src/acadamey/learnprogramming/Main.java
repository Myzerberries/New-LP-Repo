package acadamey.learnprogramming;

public class Main {

    public static void main(String[] args) {

        int myInt = 5;
        myInt++;
        System.out.println(myInt);
        myInt--;
        System.out.println(myInt);

        myInt += 3;
        System.out.println(myInt);

        myInt *= 10;
        System.out.println(myInt);
    }
}
